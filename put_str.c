#include "ft_printf.h"

int	put_str(char *s, int *p)
{
	int	i;

	i = 0;
	if (s == NULL)
	{
		write(1, "(null)", 6);
		*p += 6;
		return (*p);
	}
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
		(*p)++;
	}
	return (*p);
}
